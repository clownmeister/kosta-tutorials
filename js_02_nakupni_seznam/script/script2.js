function add(a,b) {
    return a + b
}

function subtract(a,b) {
    return a - b
}

function multiply(a,b) {
    return a * b
}
function divide(a=4,b=2,c=10) {
    return a / b / c
}
function addArray(coupleOfNumbers) {
    let result = 0
    for(const number of coupleOfNumbers) {
        result = result + number
    }
    return result
}


